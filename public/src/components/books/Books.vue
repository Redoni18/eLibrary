<template>
    <div class="users_container container">
        <h2 class="title">{{pageTitle}}</h2>
        <div class="row">
                
                <book @changePage="changePage(book._id)" v-for="book in books" :key="book._id" :title="book.title" :description="book.description" :author="book.author" :year="book.year" :imageUrl="book.imageUrl">

                </book>
        </div>

    </div>
</template>


<script>
    import book from '@/components/books/Book'
    import { mapActions, mapGetters } from 'vuex'

    export default {
        name: 'Book',

        components: { book },
        props: ['pageTitle'],

        methods: {
            ...mapActions({
                getBooks: 'getBooks'
            }),
            changePage(id){
                this.$router.push({name: 'bookDetails', params: {id: id}})
            }
        },

        mounted() {
            this.getBooks();
        },

        computed: {
            ...mapGetters({
                books: 'getBooks'
            })
        },
    }
</script>


<style scoped>
    .title {
        text-align: center;
        margin-bottom: 20px;
        margin-top: 30px;
    }

    .row{
        margin: auto;
    }

    
